BUILD_STARTED_ON	DATE,
BUILD_FINISHED_ON	DATE,
BUILD_STATUS_MSG	VARCHAR(500),
LOAD_STARTED_ON	DATE,
LOAD_FINISHED_ON	DATE,
LOAD_STATUS_MSG	VARCHAR(500),
PUBLISHED_ON	DATE,
FILE_TYPE	VARCHAR(32),
FILE_OBJECT_ID	VARCHAR(32),
FILE_DATA_LINK	DECIMAL(32,0),
JOB_TYPE	VARCHAR(25),
RERUN_FLAG	VARCHAR(1),
SIZE	int(11) NOT NULL,
REPORT_LEADING_DAYS	int(11),
REPORT_TRAILING_DAYS	int(11),
