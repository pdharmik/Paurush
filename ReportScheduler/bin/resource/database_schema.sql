DROP TABLE IF EXISTS `report_definition`;
CREATE TABLE  `report_definition` (
MDM_ID	VARCHAR(50),
MDM_LEVEL	VARCHAR(50),
REPORT_DEFINITION_ID	DECIMAL(32,0) NOT NULL,
CATEGORY_ID	DECIMAL(32,0) NOT NULL,
IS_SCHEDULABLE	VARCHAR(1),
REPORT_TYPE	VARCHAR(2),
REPORT_SOURCE_ID	VARCHAR(255),
IS_NEW	VARCHAR(1),
IS_SEND_MDM_PARAM	VARCHAR(1),
CREATED_DT 	DATE,
CREATED_BY	VARCHAR(50),
UDATED_DT 	DATE,
UPDATED_BY	VARCHAR(50)
);

DROP TABLE IF EXISTS DEFINITION_LOCALE;
CREATE TABLE  DEFINITION_LOCALE (
REPORT_DEFINITION_ID	DECIMAL(32,0) NOT NULL,
NAME	VARCHAR(255),
DESCRIPTION	VARCHAR(500),
REPORT_PARAMETER_ID	DECIMAL(32,0) NOT NULL,
SUPPORTED_LOCALE_ID	DECIMAL(32,0)
);

DROP TABLE IF EXISTS REPORT_SCHEDULE;
CREATE TABLE  REPORT_SCHEDULE (
REPORT_DEFINITION_ID	DECIMAL(32,0) NOT NULL,
REPORT_SCHEDULE_ID 	DECIMAL(32,0) NOT NULL,
RUN_FREQUENCY	VARCHAR(1),
RUN_INTERVAL	int(11),
DAY_OF_WEEK	int(11),
DAY_OF_MONTH	int(11),
SCHEDULE_EFFECTIVE_DATE	DATE,
SCHEDULE_EXPIRATION_DATE	DATE,
PREFERRED_TIMEZONE	VARCHAR(20),
COUNTRY	VARCHAR(255),
EMAIL_REMINDER_FLAG	VARCHAR(1),
RECEPIENT_EMAIL	VARCHAR(255),
USERNUMBER	VARCHAR(50),
CUSTOM_LEAD_MINUTES	int(11)
);
	
DROP TABLE IF EXISTS REPORT_PARAMETER;
CREATE TABLE  REPORT_PARAMETER (
REPORT_PARAMETER_ID	DECIMAL(32,0) NOT NULL,
NAME	VARCHAR(50),
DISPLAY_NAME	VARCHAR(50),
VALUE	VARCHAR(50),
DATA_TYPE	VARCHAR(20),
DEFAULT_VALUES	VARCHAR(255),
MAX_SIZE	DECIMAL(32,0),
IS_REQUIRED	VARCHAR(1)
);

DROP TABLE IF EXISTS JOB_RUNLOG;
