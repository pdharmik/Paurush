package com.lexmark.domain;

public class DatabaseAttachment {

	public String attachmentid;
	public String attachment_Name;
	public int attachment_size;
	public String sizeForDisplay;
	public String visibility;
	public String displayAttachmentName; 
	public String attch_identifier;
	public String requestType;
	public String getAttachmentid() {
		return attachmentid;
	}
	public void setAttachmentid(String attachmentid) {
		this.attachmentid = attachmentid;
	}
	public String getAttachment_Name() {
		return attachment_Name;
	}
	public void setAttachment_Name(String attachment_Name) {
		this.attachment_Name = attachment_Name;
	}
	public int getAttachment_size() {
		return attachment_size;
	}
	public void setAttachment_size(int attachment_size) {
		this.attachment_size = attachment_size;
	}
	public String getSizeForDisplay() {
		return sizeForDisplay;
	}
	public void setSizeForDisplay(String sizeForDisplay) {
		this.sizeForDisplay = sizeForDisplay;
	}
	public String getVisibility() {
		return visibility;
	}
	public void setVisibility(String visibility) {
		this.visibility = visibility;
	}
	public String getDisplayAttachmentName() {
		return displayAttachmentName;
	}
	public void setDisplayAttachmentName(String displayAttachmentName) {
		this.displayAttachmentName = displayAttachmentName;
	}
	public String getAttch_identifier() {
		return attch_identifier;
	}
	public void setAttch_identifier(String attch_identifier) {
		this.attch_identifier = attch_identifier;
	}
	public String getRequestType() {
		return requestType;
	}
	public void setRequestType(String requestType) {
		this.requestType = requestType;
	}
	
	
	
	

}
