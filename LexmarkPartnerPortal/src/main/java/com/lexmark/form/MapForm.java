package com.lexmark.form;

public class MapForm {
	private String mdmId;
	private String mdmLevel;
	private String formPostUrl;
	private String emailaddress;
	/**
	 * @return the mdmId
	 */
	public String getMdmId() {
		return mdmId;
	}
	/**
	 * @param mdmId the mdmId to set
	 */
	public void setMdmId(String mdmId) {
		this.mdmId = mdmId;
	}
	/**
	 * @return the mdmLevel
	 */
	public String getMdmLevel() {
		return mdmLevel;
	}
	/**
	 * @param mdmLevel the mdmLevel to set
	 */
	public void setMdmLevel(String mdmLevel) {
		this.mdmLevel = mdmLevel;
	}
	/**
	 * @return the formPostUrl
	 */
	public String getFormPostUrl() {
		return formPostUrl;
	}
	/**
	 * @param formPostUrl the formPostUrl to set
	 */
	public void setFormPostUrl(String formPostUrl) {
		this.formPostUrl = formPostUrl;
	}
	/**
	 * @return the emailaddress
	 */
	public String getEmailaddress() {
		return emailaddress;
	}
	/**
	 * @param emailaddress the emailaddress to set
	 */
	public void setEmailaddress(String emailaddress) {
		this.emailaddress = emailaddress;
	}
}
